var baseMaior = parseFloat(prompt("Digite o valor da base maior: "))
var baseMenor = parseFloat(prompt("Digite o valor da base menor: "))
var altura = parseFloat(prompt("Digite o valor da altura: "))

var areaTrapezio = ((baseMaior + baseMenor) * altura) / 2

alert("A área do trapézio é " + areaTrapezio + "cm²")